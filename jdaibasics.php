<h2>JDAI Basics</h2>
    <legend>What is JDAI?</legend>
    <p>The Juvenile Detention Alternatives Initiative (JDAI) is a nationwide effort of local and state juvenile justice systems, initiated and supported by the Annie E. Casey Foundation (AECF), to eliminate unnecessary and inappropriate use of secure detention for juveniles. Begun in 1992, JDAI has grown to become the most widely replicated juvenile justice initiative in the United States, reaching youth in more than 200 counties across 39 states as of 2014. More than one of every four young Americans lives in a community that participates in JDAI.</p>

<p>By focusing on eliminating unnecessary detention, JDAI sites seek to drive comprehensive improvements in their juvenile justice systems, including preserving public safety; minimizing re-arrest and failure-to-appear rates for youth pending adjudication; ensuring appropriate conditions of confinement in secure facilities; redirecting public finances to sustain successful reforms; and reducing racial and ethnic disparities. They pursue eight interrelated core strategies to accomplish these objectives:
<ul>
<li>Collaboration</li> 
<li>Use of accurate data</li> 
<li>Objective decision-making at detention admissions</li> 
<li>New or enhanced non-secure alternatives to detention </li> 
<li>Case processing reforms to expedite the flow of cases through the system</li> 
<li>Targeted strategies for hard-to-serve special detention populations - youth in custody as a result of probation violations, writs and warrants, as well as those awaiting placement</li> 
<li>Reducing racial disparities </li> 
<li>Improving conditions of confinement</li> 
</ul>
Visit the <a href="http://jdaihelpdesk.org/JDAI%20Starter%20Kit/Starter%20Kit%2007%20JDAI%20System%20Assessment%20Framework%20-%20Core%20Strategies%20and%20Best%20Practices%20Matrix%20Tab%206%202013.pdf">JDAI Help Desk</a> to learn more about JDAI's cores strategies and their implementations.
</p>
