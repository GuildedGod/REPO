  <h2>Getting Started</h2>
  <legend>How to Log In</legend>
 
     <p>Each JDAI site identifies an individual as the primary person who will take the lead in submitting their JDAI Annual Results Reports. Those individuals receive an invitation to this portal by email. The invitation includes a link to the portal’s main page, the name of their site as it appears in the new system, an assigned password, and the name of their site’s assigned Technical Assistance Team Leader (TATL). From the portal’s main page, click on the Site Login button. Select your site’s name from the dropdown list of sites, type in your assigned password, and click Sign In.  Please note that there is one password for each site, and it is expected to be shared as needed if more than one person will be helping to prepare that site’s Results Report. If you will be collaborating with other people to prepare the Annual Results Report for your site, you can forward your invitation email to them so that they can log in to the portal to make their contributions.  TATLs also receive emails inviting them to the portal, containing the same type of information sent to site personnel. However, because TATLs can see and make updates to the Results Reports of all the sites assigned to them, we ask that TATLs please keep their passwords confidential. </p>
<!--<p>If you have any questions about, or difficulties with, the login process, please contact us at <a href="mailto:ARR2013JDAI@SP.AECF.ORG">ARR2013JDAI@SP.AECF.ORG</a>.</p>-->

<legend>How to Create a Results Report</legend>
     <ul>
       <li>Once you&rsquo;ve logged in, you will see a dropdown list of TATLs, and two buttons: a green one that says &ldquo;Start New Report&rdquo; and a blue one that says &ldquo;Continue your existing report&rdquo;.  </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>Select       your site&rsquo;s assigned TATL from the list. A list of sites expected to submit 2014 Results Reports and their assigned TATLs is provided below for your reference. </li>
         <li>The       blue button will then become disabled (i.e. it will look grey and dim,       and if you click on it nothing will happen), and the green button will be       enabled (i.e. the color is bright and the letters are white, and if you       click on it something will happen).</li>
         <li>The       next time you come back to this page,  once you have a 2014 Results Report       in progress, and it will be the blue button, &ldquo;Continue your existing       report&rdquo;, that will be enabled.</li>
       </ul>
     </ul>
     <ul>
       <li>Click on the green button. You will be taken to a new page (that looks, for users who have previously done Results Reports, a lot like the Impact section of the Excel template that sites used in past years.  </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>You       will see your site in the Site Name field, and the TATL you selected in       the Assigned Team Leader field.  </li>
         <li>If       we know from past years what your site&rsquo;s Baseline period is, you will       also see the Baseline Period Start and Baseline Period Finish dates       already populated; and you will see the Recent Period Start and Recent       Period Finish dates populated based on what we think your Recent period       will be, carrying forward the same month-day pattern used in your       Baseline period.</li>
         <li>For       most sites, who have submitted Results Reports within the past year or       two, there will be Baseline values already filled in for the Impact indicators       on which you have reported in the past, reflecting what you told us your       baselines were at that time.  </li>
         <li>If there is no Baseline data pre-populated for your site, or if any of the data that is pre-populated is incorrect, just type the correct values in those fields as you go along.</li>
       </ul>
     </ul>
     <ul>
       <li>Fill in all of the Impact indicators. </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>You can save your work as you go simply by clicking the Save button at the bottom of the page.</li>
         <li>When you are ready to move on to the next section, go to the bottom of the page and click Save and Continue.</li>
       </ul>
     </ul>
     <ul>
       <li>That will take you to the next section, Media. </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>In       the block marked Add Media, for the first media mention that you want to report, type a brief description, title or hyperlink in the Media Name       field; choose a date and a media type; then click Add New Media. </li>
         <li>You       will stay on the same page, but the information you just added will now be displayed in a new list of Media Coverage, shown above the Add Media block.  To keep adding items, just repeat those same steps.</li>
         <li>To       modify a media item that you&rsquo;ve already entered, go to the Edit Media block, and select the item you want to change from the Item to Edit dropdown       list; make your changes; and click Update Media Entry.</li>
         <li>When       you&rsquo;re ready to move on to the next section, click Continue.</li>
         <ul type="square">
           <li>In this section of the report, as well as the Training and Leverage        sections, you don&rsquo;t have to click Save to save your work as you go along, because each time you click Add New Media or Update Media Entry your        changes are being saved.</li>
         </ul>
       </ul>
     </ul>
     <ul>
       <li>That will take you to the Training section of the report, which works the same way as Media.  </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>For       the first JDAI-Specific Training that you want to report, in the Add       Training block type the training title or other short description in the       Description of Training field; record the date on which the training took       place; how many people were trained; and the JDAI Focus Area to which the       training was most closely related (your options being: one of the 8 Core       Strategies; State Scale Expansion; and Other). Then click Add New       Training.</li>
         <li>Follow       the same procedure you used in the Media section to add more trainings,       or to edit any that you&rsquo;ve already entered. </li>
         <li>When       you&rsquo;re ready to move on to the next section, click Continue.</li>
       </ul>
     </ul>
     <ul>
       <li>That takes you to the Leverage section, which also works the same way as Media. </li>
       <li>That takes you to the Reforms section, which is different from the other sections and different from what has been asked of sites in years past.  </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>You&rsquo;ll       see a list of specific detention reforms, grouped by Core Strategy, which       JDAI sites have employed over the years with good or promising results.       These are all items on the Core Strategies &amp; Best Practices matrix that       can be found on the JDAI Help Desk (<a href="http://jdaihelpdesk.org/JDAI%20Starter%20Kit/Starter%20Kit%2007%20JDAI%20System%20Assessment%20Framework%20-%20Core%20Strategies%20and%20Best%20Practices%20Matrix%20Tab%206%202013.pdf">here</a>) and that JDAI is increasingly trying to use as a kind or       organizing framework for our training &amp; technical assistance.</li>
         <li>You       will see on the page a description of the scale you should use to rate       your site&rsquo;s extent of implementation for each reform. To see a brief       description of each reform, click on the question mark icon next to each reform       on the list. </li>
         <li>Please       rate the extent to which your site, to the best of your knowledge, has       implemented each practice as of the point in time at which you&rsquo;re       completing the Results Report. </li>
         <li>You       can save your changes as you go along by clicking Save; and when you&rsquo;re       ready to move on click Save and Continue.</li>
       </ul>
     </ul>
     <ul>
       <li>That takes you to a section called Final Notes that contains a summary of everything you&rsquo;ve entered up to this point, and below that two text boxes: </li>
     </ul>
     <ul type="disc">
       <ul type="circle">
         <li>One       labeled Report Content Feedback, where you should record any explanations       that you think your TATL or JDAI management should know about anything       you entered in your report; and </li>
         <li>One       labeled New Portal Interface Feedback, where you can tell us what&rsquo;s       working or not &amp; what you&rsquo;d like to see different in the website       itself.  </li>
         <li>When       you&rsquo;re done, click the green button, Save Report and Submit to Your TATL.</li>
       </ul>
     </ul>
     <ul>
       <li>That takes you to a new page that will say &ldquo;Success!&rdquo; – and that means your report is now queued up and awaiting your TATL&rsquo;s review.  There will also be a blue button labeled Return Home, and if you click on it, you will return back to where you started.</li>
     </ul>

<legend>What Happens When You Submit Your Results Report</legend>
<p>When a site submits a Results Report to their TATL, it gets added to a queue of all of that TATL’s assigned sites. They will be able to look at that list and see which of their sites have started a report and which ones have not; and they will be able to see what reports are submitted and ready for their review. They will then be able to go in and look at your report online. If they have questions or concerns about it, they may contact you directly (e.g. by phone or email), or they may record their questions on the Final Notes section of the report and change its status from “Submitted” to “In Progress”. If they do that, your TATL will notify you about it (in the next Results Reporting cycle the notification will happen automatically) and you can go back into the report, make any necessary corrections, and re-submit it. When your TATL is satisfied with the report, they will mark it as “Completed”, which will prompt JDAI management to give it a final review. When the report is judged to be as complete and accurate as possible, it will be marked “Final” and at that point it will be included in the analysis for the 2014 Annual Results Report Summary document, which will be available at the 2015 JDAI Inter-Site Conference.</p>

